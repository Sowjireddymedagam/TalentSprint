// >, <, >=, <=, ==, ===, !=, !==

let num1 = 23;
let num2 = 22;
let result = num1 > num2;
console.log(`${num1} > ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 < num2;
console.log(`${num1} < ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 >= num2;
console.log(`${num1} >= ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 <= num2;
console.log(`${num1} <= ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);

result = num1 == num2;
console.log(`${num1} == ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 === num2;
console.log(`${num1} === ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 != num2;
console.log(`${num1} != ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);
result = num1 !== num2;
console.log(`${num1} !== ${num2} is: ${result} and Type of result is : ${typeof(result)}`, result);